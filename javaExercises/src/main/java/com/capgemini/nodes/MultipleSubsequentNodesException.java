package com.capgemini.nodes;

public class MultipleSubsequentNodesException extends NodeException {
	public MultipleSubsequentNodesException() {
		super();
	}

	public MultipleSubsequentNodesException(String message) {
		super(message);
	}

	public MultipleSubsequentNodesException(String message, Throwable cause) {
		super(message, cause);
	}

	public MultipleSubsequentNodesException(Throwable cause) {
		super(cause);
	}
}
